int gcd(int a, int b){
    if(b == 0) return a;
    return gcd(b, a % b);
}

int main(){
    fastio;
    int n;
    cin >> n;
    vi vec(n);
    for(int i{0}; i < n; ++i){
        cin >> vec[i];
    }
    int g{0};
    sort(vec.begin(), vec.end());
    for(int i{0}; i < n - 1; ++i){
        g = gcd(g, vec[i + 1] - vec[i]);
    }
    int k = (vec.back() - vec.front()) / g;
    cout << k + 1 - n;
}